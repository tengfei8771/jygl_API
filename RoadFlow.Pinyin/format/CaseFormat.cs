namespace RoadFlow.Pinyin.format
{
    /// <summary>
    /// 大小写格式
    /// </summary>
    public enum CaseFormat
    {
        /// <summary>
        /// 首字母大写，此选项对 a e o 几个独音无效
        /// </summary>
        CAPITALIZE_FIRST_LETTER,
        /// <summary>
        /// 全小写
        /// </summary>
        LOWERCASE,
        /// <summary>
        /// 全大写
        /// </summary>
        UPPERCASE
    }
}
